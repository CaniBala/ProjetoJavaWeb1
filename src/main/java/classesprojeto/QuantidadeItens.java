package classesprojeto;

public class QuantidadeItens {

	private int quantCadeiras;
	private int quantComputadores;
	private int quantItens;
	
	
	public int getQuantItens() {
		return quantItens;
	}
	public void setQuantItens(int quantItens) {
		this.quantItens = quantItens;
	}
	public int getQuantCadeiras() {
		return quantCadeiras;
	}
	public void setQuantCadeiras(int quantCadeiras) {
		this.quantCadeiras = quantCadeiras;
	}
	public int getQuantComputadores() {
		return quantComputadores;
	}
	public void setQuantComputadores(int quantComputadores) {
		this.quantComputadores = quantComputadores;
	}
	
	
}
